package model.order;

import java.sql.Date;

/**
 *
 * @author droperdev
 */
public class Order {

    private int id;
    private int clientId;
    private int addressId;
    private int statusId;
    private int voucherId;
    private int orderTypeId;
    private int paymentMethodId;
    private int distributorId;
    private Date createdAt;
    private Date updatedAt;

    public Order() {
    }

    @Override
    public String toString() {
        return "Order{" + "id=" + id + ", clientId=" + clientId + ", addressId=" + addressId + ", statusId=" + statusId + ", voucherId=" + voucherId + ", orderTypeId=" + orderTypeId + ", paymentMethodId=" + paymentMethodId + ", distributorId=" + distributorId + ", createdAt=" + createdAt + ", updatedAt=" + updatedAt + '}';
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getClientId() {
        return clientId;
    }

    public void setClientId(int clientId) {
        this.clientId = clientId;
    }

    public int getAddressId() {
        return addressId;
    }

    public void setAddressId(int addressId) {
        this.addressId = addressId;
    }

    public int getStatusId() {
        return statusId;
    }

    public void setStatusId(int statusId) {
        this.statusId = statusId;
    }

    public int getVoucherId() {
        return voucherId;
    }

    public void setVoucherId(int voucherId) {
        this.voucherId = voucherId;
    }

    public int getOrderTypeId() {
        return orderTypeId;
    }

    public void setOrderTypeId(int orderTypeId) {
        this.orderTypeId = orderTypeId;
    }

    public int getPaymentMethodId() {
        return paymentMethodId;
    }

    public void setPaymentMethodId(int paymentMethodId) {
        this.paymentMethodId = paymentMethodId;
    }

    public int getDistributorId() {
        return distributorId;
    }

    public void setDistributorId(int distributorId) {
        this.distributorId = distributorId;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }

}
